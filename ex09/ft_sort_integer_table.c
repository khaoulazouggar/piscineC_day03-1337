void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int j;
	int swap;

	j = 0;
	while (j < size)
	{
		i = 0;
		while (i < size - j - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				swap = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = swap;
			}
			i++;
		}
		j++;
	}
}
